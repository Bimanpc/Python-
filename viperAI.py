def is_same_host(start_url: str, new_url: str) -> bool:
    a = urlparse.urlparse(start_url)
    b = urlparse.urlparse(new_url)
    return a.netloc == b.netloc


def normalize_url(base_url: str, link: str) -> str:
    link = link.strip()
    if not link:
        return ""
    abs_url = urlparse.urljoin(base_url, link)
    parsed = urlparse.urlparse(abs_url)
    if parsed.scheme not in ("http", "https"):
        return ""
    # remove fragments, normalize
    cleaned = parsed._replace(fragment="").geturl()
    return cleaned


def fetch_url(url: str) -> tuple[int, str, str]:
    headers = {"User-Agent": USER_AGENT}
    try:
        resp = requests.get(url, headers=headers, timeout=REQUEST_TIMEOUT)
    except requests.RequestException:
        return 0, "", ""

    content_type = resp.headers.get("Content-Type", "").split(";")[0].strip().lower()
    if content_type not in ALLOWED_CONTENT_TYPES:
        return resp.status_code, "", content_type

    return resp.status_code, resp.text, content_type


def extract_links(base_url: str, html: str) -> list:
    soup = BeautifulSoup(html, "html.parser")
    links = []
    for a in soup.find_all("a", href=True):
        norm = normalize_url(base_url, a["href"])
        if norm:
            links.append(norm)
    return links


def save_result(data: PageSEOData):
    """
    Persist or print results.
    For now, just print JSON to stdout.
    Replace with DB/file storage as needed.
    """
    print(json.dumps(asdict(data), ensure_ascii=False, indent=2))


def crawl(start_url: str, max_pages: int = MAX_PAGES):
    visited = set()
    q = queue.Queue()
    q.put(start_url)

    count = 0

    while not q.empty() and count < max_pages:
        url = q.get()
        if url in visited:
            continue
        visited.add(url)

        print(f"\n[CRAWL] Fetching: {url}")
        status_code, html, content_type = fetch_url(url)
        print(f"[CRAWL] Status: {status_code}, Content-Type: {content_type}")

        if status_code != 200 or not html:
            continue

        # Parse + LLM SEO analysis
        data = parse_page(url, html, status_code)
        save_result(data)

        # Extract internal links
        for link in extract_links(url, html):
            if is_same_host(start_url, link) and link not in visited:
                q.put(link)

        count += 1
        time.sleep(CRAWL_DELAY_SEC)


