# -----------------------------
# HTML parsing
# -----------------------------

def extract_text(soup: BeautifulSoup) -> str:
    for tag in soup(["script", "style", "noscript"]):
        tag.decompose()
    text = soup.get_text(separator=" ")
    text = re.sub(r"\s+", " ", text).strip()
    return text


def parse_page(url: str, html: str, status_code: int) -> PageSEOData:
    soup = BeautifulSoup(html, "html.parser")

    title_tag = soup.find("title")
    title = title_tag.get_text(strip=True) if title_tag else ""

    meta_desc_tag = soup.find("meta", attrs={"name": "description"})
    meta_description = meta_desc_tag.get("content", "").strip() if meta_desc_tag else ""

    h1 = [h.get_text(strip=True) for h in soup.find_all("h1")]
    h2 = [h.get_text(strip=True) for h in soup.find_all("h2")]
    h3 = [h.get_text(strip=True) for h in soup.find_all("h3")]

    text = extract_text(soup)
    word_count = len(text.split())

    prompt = build_llm_prompt(url, text, title, meta_description)
    llm_resp = call_llm(prompt)

    return PageSEOData(
        url=url,
        status_code=status_code,
        title=title,
        meta_description=meta_description,
        h1=h1,
        h2=h2,
        h3=h3,
        word_count=word_count,
        llm_summary=llm_resp.get("summary", ""),
        llm_keywords=llm_resp.get("keywords", []),
        llm_notes=llm_resp.get("notes", ""),
    )


